    <p>
    O laço do While é semelhante ao laço while. A principal diferença entre os dois, é que estae testa se a condiçao de execuçao é verdadeira antes de executar os comandos, enquanto aquele executa os comando primeiro, e testa a condição de execução depois.
    </p>
    <p>
      Por testar a condição de execução apos os comandos terem sido executados, o <i>do/while</i> garante que estes comandos serão executaso ao menos uma vez, pois, primeiro ele executa os comandos, e somente depois, ele testa a consdição para efetuar a repetição.
    </p>
