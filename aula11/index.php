    <h2>Laços de Repetição</h2>

    <p>
        Utilizamos laços ou estrutura de repetição quando precisamos executar determinados trechos de codigos repetidas vezes. A quantidade de execuções de um laço depende sempre de uma condição de execução. Enquanto esta condição for <b> verdadeira</b>, Os comando especifcados entre as chaves do laço serão executados.
    </p><p>
        Após serem executados, o laço de repetição irá testar se sua condição de execução ainda é verdadeira. Se for, os comandos são executados novamente.
        Porem, caso a condição passe a ser <b>falsa</b>, o laço deixa de exetuar os comando especificados.
    </p>

    <p><h3>No PHP, existem 4 tipos básicos de laços de repetição:</h3>

        <ul>
            <li><a href="for/index.php">FOR (PARA)</a></li>
            <li><a href="while/index.php">WHILE (ENQUANTO)</a></li>
            <li>DO/WHILE (FAÇA/ENQUANTO)</li>
            <li>FOREACH (PARA CADA - ESPECIFICO PARA ARREYS)</li>
        </ul>
    </p>
    
